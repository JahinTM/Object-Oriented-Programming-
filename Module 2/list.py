num = [10,20,30,40,50,60,70,80,90]

print(num[3], num[-3])

print(num[2:6]) # start : End

print(num[1:8:2]) # start : End : step
print(num[8:1:-1]) # ulta asbe
print(num[4:]) # 4th index  to end
print(num[:6]) # start to 6th index
print(num[:]) #shortcut to copy
print(num[::-1])  #shortcut to reverse